#include "3-calc.h"

/**
 * op_add - adds numbers
 * @a: number 1
 * @b: number 2
 * Return: a + b
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtracts numbers
 * @a: number 1
 * @b: number 2
 * Return: a - b
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplys numbers
 * @a: number 1
 * @b: number 2
 * Return: a * b
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divides numbers
 * @a: number 1
 * @b: number 2
 * Return: a / b
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - mods numbers
 * @a: number 1
 * @b: number 2
 * Return: a - b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
